// ========== 外观描述文本生成 ==========
CharaTemplates.generateAppearanceDesc = function(c) {
    const defs = window.APPEARANCE_DESC_DEFS || {};
    const get = (map, key) => (defs[map] && defs[map][key]) ? defs[map][key] : '';

    const race = get('race', c.talent[314]) || '人类';
    const age = Math.max(14, (c.level || 1) + 14 + RAND_RANGE(-2, 3));
    const bodyType = get('bodyType', c.talent[308]) || '匀称';
    const hairColor = get('hairColor', c.talent[300]) || '黑';
    const hairLength = get('hairLength', c.talent[302]) || '短发';
    const hairStyle = get('hairStyle', c.talent[303]) || '';
    const hairQuality = get('hairQuality', c.talent[301]) || '';
    const bangs = get('hasBangs', c.talent[304]) || '';
    const eyeShape = get('eyeShape', c.talent[305]) || '';
    const eyeColor = get('eyeColor', c.talent[306]) || '黑';
    const lipShape = get('lipShape', c.talent[307]) || '';

    // 性别
    let gender = '女性';
    if (c.talent[122]) gender = '男性';
    else if (c.talent[121]) gender = '扶她';

    // 构建描述段落
    let parts = [];
    parts.push(`一名${age}岁的${race}${gender}`);

    // 体型
    let bodyText = '';
    if (c.talent[99]) bodyText = '身材魁梧健壮';
    else if (c.talent[100]) bodyText = '身材娇小玲珑';
    else bodyText = `身材${bodyType}`;
    parts.push(`，${bodyText}`);

    // 头发
    let hairParts = [];
    if (hairQuality) hairParts.push(hairQuality);
    hairParts.push(`${hairColor}色`);
    hairParts.push(hairLength);
    if (hairStyle && hairStyle !== '散发') hairParts.push(`梳成${hairStyle}`);
    let hairText = `留着${hairParts.join('')}`;
    if (bangs) hairText += `，${bangs}`;
    parts.push(`，${hairText}`);

    // 眼睛
    let eyeText = '';
    if (eyeShape) eyeText = `${eyeShape}中映着${eyeColor}色的光芒`;
    else eyeText = `一双${eyeColor}色的眼睛`;
    parts.push(`，${eyeText}`);

    // 唇
    if (lipShape) parts.push(`，${lipShape}`);
    parts.push('。');

    // 特殊特征
    if (c.talent[48]) parts.push('鼻梁上架着一副眼镜。');
    if (c.talent[79]) parts.push('言行举止透着一股男孩子气。');

    const desc = parts.join('');
    c.cstr[330] = desc;

    // 生成并存储身体数据（固定值，不随页面切换变化）
    const bodyAge = age;
    let height, weight, bust, waist, hip;
    if (c.talent[100]) { height = 140 + RAND(10); weight = 35 + RAND(8); }
    else if (c.talent[99]) { height = 170 + RAND(15); weight = 60 + RAND(20); }
    else { height = 155 + RAND(20); weight = 45 + RAND(20); }
    if (c.talent[116]) bust = 70 + RAND(5);
    else if (c.talent[109]) bust = 75 + RAND(8);
    else if (c.talent[110]) bust = 90 + RAND(15);
    else if (c.talent[114]) bust = 105 + RAND(20);
    else bust = 80 + RAND(10);
    waist = Math.floor(bust * 0.65) + RAND(5);
    hip = Math.floor(bust * 0.85) + RAND(8);
    c.cstr[331] = `${bodyAge}`;
    c.cstr[332] = `${height}`;
    c.cstr[333] = `${weight}`;
    c.cstr[334] = `${bust}/${waist}/${hip}`;

    // 生成身体特征中文描述
    let cup, waistDesc, hipDesc, faceDesc;
    if (bust < 75) cup = 'A罩杯';
    else if (bust <= 80) cup = 'B罩杯';
    else if (bust <= 90) cup = 'C罩杯';
    else if (bust <= 100) cup = 'D罩杯';
    else if (bust <= 110) cup = 'E罩杯';
    else cup = 'F罩杯';

    if (waist < 60) waistDesc = '细腰';
    else if (waist <= 65) waistDesc = '蛮腰';
    else if (waist <= 70) waistDesc = '腰肢匀称';
    else waistDesc = '腰肢丰腴';

    if (hip < 85) hipDesc = '臀部扁平';
    else if (hip <= 92) hipDesc = '臀部挺翘';
    else hipDesc = '臀部丰腴';

    // 颜值描述
    if (c.talent[91] || c.talent[92]) faceDesc = '容貌绝美';
    else if (c.talent[93]) faceDesc = '面容威严';
    else if (c.talent[48]) faceDesc = '知性清秀';
    else if (c.talent[79]) faceDesc = '英气逼人';
    else {
        const faceRoll = RAND(6);
        const faces = ['清秀可人', '相貌平平', '甜美可爱', '冷艳高贵', '眉眼端正', '气质独特'];
        faceDesc = faces[faceRoll];
    }

    c.cstr[335] = `${cup} · ${waistDesc} · ${hipDesc} · ${faceDesc}`;

    return desc;
};

